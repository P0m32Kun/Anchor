// defaultDomainSet is a pre-computed set for O(1) lookups.
var defaultDomainSet map[string]struct{}

func init() {
	defaultDomainSet = make(map[string]struct{}, len(DefaultDomains))
	for _, d := range DefaultDomains {
		defaultDomainSet[strings.ToLower(d)] = struct{}{}
	}
}

// IsDefaultDomain returns true if the domain is in the built-in exclusion list.
func IsDefaultDomain(domain string) bool {
	domain = strings.ToLower(strings.TrimSpace(domain))
	// Strip port if present
	if idx := strings.LastIndex(domain, ":"); idx > 0 {
		domain = domain[:idx]
	}
	_, ok := defaultDomainSet[domain]
	return ok
}
